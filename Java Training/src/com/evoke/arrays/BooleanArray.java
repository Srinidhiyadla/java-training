package com.evoke.arrays;

import java.util.Arrays;

public class BooleanArray {

	public static void main(String[] args) {
		boolean bool_Array[] = new boolean[5];
		bool_Array[0] = false;
		bool_Array[1] = true;
		bool_Array[2] = false;
		bool_Array[3] = true;
		
		System.out.println("Boolean array Example:");
		System.out.print(Arrays.toString(bool_Array));

	}

}
